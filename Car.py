class Car:
    def __init__(self,m, mo, y):
        self.__make = m
        self.__model = mo
        self.__year = y

    def get_make(self):
        return self.__make

    def get_model(self):
        return self.__model

    def get_year(self):
        return self.__year
    
    def set_make(self, m1):
        self.__make = m1

    def set_model(self, mo1):
        self.__model = mo1

    def set_year(self, y1):
        self.__year = y1
















































